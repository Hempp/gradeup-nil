function replaceEmojiWithIcon(text, size = 16) {
  let result = text;
  for (const [emoji, iconName] of Object.entries(EmojiToIcon)) {
    if (result.includes(emoji)) {
      const iconHtml = getIcon(iconName, size, 'inline-icon');
      result = result.split(emoji).join(iconHtml);
    }
  }
  return result;
}

