// Helper function to get icon with custom size and color
function getIcon(name, size = 24, className = '') {
  const icon = Icons[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }

  const sizeAttr = `width="${size}" height="${size}"`;
  const classAttr = className ? `class="${className}"` : '';

  return icon.replace('<svg', `<svg ${sizeAttr} ${classAttr}`);
}

// Helper function to create icon element
function createIconElement(name, size = 24, className = '') {
  const wrapper = document.createElement('span');
  wrapper.innerHTML = getIcon(name, size, className);
  wrapper.className = `icon-wrapper ${className}`;
  return wrapper;
}

