// Function to replace emojis in a specific element
function replaceEmojisInElement(element) {
  // Skip script, style, and svg elements
  if (['SCRIPT', 'STYLE', 'SVG', 'NOSCRIPT'].includes(element.tagName)) {
    return;
  }

  // Process text nodes
  const walker = document.createTreeWalker(
    element,
    NodeFilter.SHOW_TEXT,
    null,
    false
  );

  const textNodes = [];
  while (walker.nextNode()) {
    textNodes.push(walker.currentNode);
  }

  textNodes.forEach(textNode => {
    const text = textNode.textContent;
    let hasEmoji = false;

    for (const emoji of Object.keys(EmojiToIcon)) {
      if (text.includes(emoji)) {
        hasEmoji = true;
        break;
      }
    }

    if (hasEmoji) {
      const span = document.createElement('span');
      span.innerHTML = replaceEmojiWithIcon(text, 16);
      textNode.parentNode.replaceChild(span, textNode);
    }
  });
}

// Function to replace all emojis in the document
function replaceAllEmojis() {
  // Specific icon containers that should use SVG icons
  const iconContainers = document.querySelectorAll(`
    .step-icon, .feature-icon, .stat-icon, .nav-icon,
    .category-icon, .role-icon, .form-icon, .export-icon,
    .result-icon, .qa-icon, .cat-icon, .team-icon,
    .report-icon, .earning-icon, .brand-logo, .donor-icon,
    .payment-icon, .method-icon, .upload-icon, .notif-icon,
    .activity-icon, .metric-icon, .card-icon, .cert-icon,
    .benefit-icon, .tax-icon, .msg-avatar, .profile-avatar,
    .social-icon, .video-icon, .search-icon, .check, .cross
  `);

  iconContainers.forEach(container => {
    const text = container.textContent.trim();
    const iconName = EmojiToIcon[text];

    if (iconName) {
      // Determine icon size based on container
      let size = 20;
      if (container.classList.contains('step-icon')) size = 28;
      else if (container.classList.contains('feature-icon')) size = 24;
      else if (container.classList.contains('stat-icon')) size = 24;
      else if (container.classList.contains('profile-avatar')) size = 24;
      else if (container.classList.contains('upload-icon')) size = 28;
      else if (container.classList.contains('brand-logo')) size = 24;
      else if (container.classList.contains('nav-icon')) size = 18;
      else if (container.classList.contains('qa-icon')) size = 16;
      else if (container.classList.contains('notif-icon')) size = 16;

      container.innerHTML = getIcon(iconName, size);
    }
  });

  // Replace checkmarks specifically
  document.querySelectorAll('.check, .verified-check, .check-icon, .step-check').forEach(el => {
    const text = el.textContent.trim();
    if (text === '‚úì' || text === '‚úî' || text === '‚úîÔ∏è') {
      el.innerHTML = getIcon('check', 16);
    }
  });

  // Replace crosses
  document.querySelectorAll('.cross').forEach(el => {
    const text = el.textContent.trim();
    if (text === '‚úó' || text === '‚úò' || text === '‚ùå') {
      el.innerHTML = getIcon('cross', 16);
    }
  });
}

// Initialize on DOM ready
if (typeof document !== 'undefined') {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', replaceAllEmojis);
  } else {
    // DOM already loaded
    replaceAllEmojis();
  }
}

// Expose for manual calling
if (typeof window !== 'undefined') {
  window.replaceAllEmojis = replaceAllEmojis;
  window.replaceEmojisInElement = replaceEmojisInElement;
}
